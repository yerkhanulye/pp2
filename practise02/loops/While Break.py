# 1. Exit at specific number
i = 1
while i < 10:
    if i == 5: break
    print(i)
    i += 1

# 2. User input simulation
while True:
    print("Looping...")
    break # Exit immediately

# 3. Search limit
val = 0
while val < 100:
    if val == 20: break
    val += 10

# 4. Finding a condition
x = 1
while x < 50:
    if x % 7 == 0: break
    x += 1