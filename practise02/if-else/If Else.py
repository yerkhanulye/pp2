# 1. Simple binary choice
if 5 > 10:
    print("Yes")
else:
    print("No")

# 2. Temperature check
temp = 20
if temp > 25:
    print("Hot")
else:
    print("Cool")

# 3. String length check
name = "Bob"
if len(name) > 5:
    print("Long name")
else:
    print("Short name")

# 4. Dictionary key check
data = {"id": 1}
if "name" in data:
    print("Name exists")
else:
    print("Name missing")

# 5. Even or Odd check
x = 4
if x % 2 == 0:
    print("Even")
else:
    print("Odd")