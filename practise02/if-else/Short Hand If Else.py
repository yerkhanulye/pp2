# 1. Short if
if 1 > 0: print("Always True")

# 2. Simple ternary
print("Hi") if True else print("Bye")

# 3. Assigning variable
msg = "Access Granted" if True else "Access Denied"

# 4. Ternary in print
x = 10
print("Big") if x > 100 else print("Small")

# 5. Ternary with calculation
val = 10 if x > 5 else 0