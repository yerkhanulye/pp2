# 1. Multi-stage score check
score = 75
if score > 90: print("A")
elif score > 70: print("B")
else: print("C")

# 2. Time of day
hour = 14
if hour < 12: print("Morning")
elif hour < 18: print("Afternoon")
else: print("Evening")

# 3. Comparing two numbers
x, y = 5, 10
if x > y: print("x is bigger")
elif y > x: print("y is bigger")
else: print("Equal")

# 4. File type check
ext = ".jpg"
if ext == ".py": print("Python")
elif ext == ".jpg": print("Image")
else: print("Unknown")

# 5. Age groups
age = 15
if age < 13: print("Child")
elif age < 20: print("Teen")
else: print("Adult")